# This will be the file that contains the functions for the lists
# Lists are data structures that group sequencs of elements
# Created using square brackets
my_list = [1, 2, 3, 4, 5]

# can access values in the list using the index (note it is zero indexed)
print(my_list[3]) # prints 4

# perhaps you want a small portion of a list, you can use the slice operator
print(my_list[1:3]) # prints [2, 3]

# Mathematic operations can be performed on lists
print([1,2] + [3,4]) # prints [1, 2, 3, 4]



